# Title: 155. Min Stack
# URL: https://leetcode.com/problems/min-stack/

class MinStack:

    def __init__(self):
        self.stack = []
        self.min_stack = []

    def push(self, val: int) -> None:
        self.stack.append(val)
        if not self.min_stack or val <= self.min_stack[-1]:
            self.min_stack.append(val)

    def pop(self) -> None:
        if self.stack:
            val = self.stack.pop()
            if val == self.min_stack[-1]:
                self.min_stack.pop()

    def top(self) -> int:
        return self.stack[-1] if self.stack else None

    def getMin(self) -> int:
        return self.min_stack[-1] if self.min_stack else None


sol = MinStack()
sol.push(-2)
sol.push(0)
sol.push(-3)
print(sol.getMin())  # return -3
sol.pop()
print(sol.top())     # return 0
print(sol.getMin())  # return -2


